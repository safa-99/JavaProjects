public class Card {
	
	int id;
	String header;
	String description;
	double rate;
	
	public Card() {
		
	}
	
	public Card(int id, String header, String description, double rate) {
		
		this.id = id;
		this.header = header;
		this.description = description;
		this.rate = rate;
	}
	
	

}
